#Augmented Assignment operator
abe_1 = 20
print('First Abe Value', abe_1)
abe_1 = abe_1 + 10
print('Second Abe Value', abe_1)
abe_1 += 10
print('Third Abe Value', abe_1)
abe_1 -= 10
print('Fourth Abe Value', abe_1)
abe_1 /= 10
print('Fifth Abe Value', abe_1)
abe_1 *= 10
print('Sixth Abe Value', abe_1)